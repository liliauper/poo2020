    private void btnActualizarClientesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarClientesActionPerformed
        // Evento boton actualizar clientes:
        if (txtActualizarClientes.getText().equals("")) {
            lblInfo.setText("Ingresa id del cliente que quieres actualizar");
            return;

        }

        int id = Integer.parseInt(txtActualizarClientes.getText());
        if (txtActualizarClientes.getText().equals("")) {
            lblInfo.setText("Ingresa id del cliente que quieres actualizar");
            return;

        }

        Cliente cliente = clienteRep.getById(id);
        if (cliente.getId() == 0) {
            lblInfo.setText("No existe cliente con Id: " + id);
            return;
        }
        if (!validarActualizar()) {
            return;
        }

        cliente.setNombre(txtNombreA.getText());
        cliente.setApellido(txtApellidoA.getText());
        cliente.setDni_cuit(txtDniA.getText());
        cliente.setPais(txtPaisA.getText());
        cliente.setProvincia(txtProvinciaA.getText());
        cliente.setDireccion(txtDireccionA.getText());
        cliente.setTelefono(txtTelA.getText());
        cliente.setMail(txtMailA.getText());
        cliente.setFechaNacimiento(txtFechaNacA.getText());
        cliente.setContrasenia(txtPassA.getText());

        clienteRep.update(cliente);
        lblInfo.setText(cliente.getNombre() + " " + cliente.getApellido() + ", con  id: " + cliente.getId() + " se actualizo correctamente ");
        limpiarFormActualizarClientes();
        txtActualizarClientes.setEditable(true);
    }//GEN-LAST:event_btnActualizarClientesActionPerformed

