    private void btnEliminarClientesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarClientesActionPerformed

        // Eliminar clientes
        if (txtVerEliminarClientes.getText().equals("")) {
            lblInfo.setText("Ingresa id del usuario que quieres eliminar");
            return;
        }

        int id = Integer.parseInt(txtVerEliminarClientes.getText());

        Cliente cliente = clienteRep.getById(id);
        if (cliente.getId() == 0) {
            lblInfo.setText("No existe cliente con Id: " + id);
            return;

        }
        if (JOptionPane.showConfirmDialog(this, "Estas seguro?") != 0) {
            return;
        }
        clienteRep.remove(cliente);
        lblInfo.setText(cliente.getNombre() + " " + cliente.getApellido() + ", con id: " + id + ", fue removido con exito");
        limpiarFormEliminarClientes();
        txtVerEliminarClientes.requestFocus();
        txtVerEliminarClientes.setEditable(true);

    }//GEN-LAST:event_btnEliminarClientesActionPerformed

