    private void btnEliminarCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarCActionPerformed
        // TODO add your handling code here:
        int fila = tblClientes.getSelectedRow();
        if (fila == -1) {
            lblInfo.setText("Seleccione una fila");
            return;
        }
        int id = (int) tblClientes.getValueAt(fila, 0);
        if (JOptionPane.showConfirmDialog(this, "Estas seguro"
                + " que quieres eliminar cliente id:" + id + "?") != 0) {
            return;
        }
        clienteRep.remove(clienteRep.getById(id));
        lblInfo.setText("Cliente con id: " + id + ", fue removido con exito");
        new Table().cargar(tblClientes, clienteRep.getAll());


    }//GEN-LAST:event_btnEliminarCActionPerformed

