    private boolean validarIngresar() {
        if (!new Validator(txtNombre).length(3, 50)) {
            return false;
        }
        if (!new Validator(txtApellido).length(3, 50)) {
            return false;
        }
        if (!new Validator(txtDni).length(7)) {
            return false;
        }
        if (!new Validator(txtPais).length(3, 50)) {
            return false;
        }
        if (!new Validator(txtProvincia).length(3, 50)) {
            return false;
        }
        if (!new Validator(txtDireccion).length(3, 50)) {
            return false;
        }
        if (!new Validator(txtTel).length(7)) {
            return false;
        }
        if (!new Validator(txtMail).length(3, 50)) {
            return false;
        }

        return true;
    }
