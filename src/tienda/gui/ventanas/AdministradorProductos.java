    private void btnAltaProductosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAltaProductosActionPerformed
        //  //Evento boton Ingresar articulos:
        if (!validarIngresar()) {
            return;
        }
        if (artxtDescripcion.getText().length() > 1000) {
            lblInfo.setText("El texto no puede superar 1000 caracteres");
            return;
        }
        if (artxtDetalles.getText().length() > 1000) {
            lblInfo.setText("El texto no puede superar 1000 caracteres");
            return;
        }

        Producto producto = new Producto(
                Integer.parseInt(txtCodigo.getText()),
                txtNombre.getText(),
                Float.parseFloat(txtPrecio.getText()),
                Integer.parseInt(txtCantidad.getText()),
                txtImagen.getText(),
                artxtDescripcion.getText(),
                artxtDetalles.getText(),
                cmbDisponible.getItemAt(cmbDisponible.getSelectedIndex())
        );
        prodRepository.save(producto);
        lblInfo.setText("El producto se ingreso correctamente con Id: " + producto.getId());

        limpiarFormAlta();


    }//GEN-LAST:event_btnAltaProductosActionPerformed
