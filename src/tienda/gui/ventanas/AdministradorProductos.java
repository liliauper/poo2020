    private void btnActualizarPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarPActionPerformed
        // Boton actualizar articulos:
         if (txtModificarProductos.getText().equals("")) {
            lblInfo.setText("Ingresa id de articulo que quieres actualizar");
            return;

        }

        
        lblInfo.setText("");

       

        int id = Integer.parseInt(txtModificarProductos.getText());

        Producto producto = prodRepository.getById(id);
        if (producto.getId() == 0) {
            lblInfo.setText("No existe articulo con Id: " + id);
            limpiarFormActualizar();
            return;
        }
        if (!validarModificar()) {
            return;
        }
        if (artxtDescripcionM.getText().length() > 1000) {
            lblInfo.setText("El texto no puede superar 1000 caracteres");
            return;
        }
        if (artxtDetallesM.getText().length() > 1000) {
            lblInfo.setText("El texto no puede superar 1000 caracteres");
            return;
        }

        producto.setCod_productos(Integer.parseInt(txtCodigoM.getText()));
        producto.setNombre(txtNombreM.getText());
        producto.setPrecio(Float.parseFloat(txtPrecioM.getText()));
        producto.setCantidad(Integer.parseInt(txtCantidadM.getText()));
        producto.setImagen(txtImagenM.getText());
        producto.setDescripcion(artxtDescripcionM.getText());
        producto.setDetalles(artxtDetallesM.getText());
        producto.setDisponible(cmbDisponibleM.getItemAt(cmbDisponibleM.getSelectedIndex()));

        prodRepository.update(producto);
        lblInfo.setText("El producto " + producto.getId() + " se actualizo correctamente ");

        limpiarFormActualizar();
        txtModificarProductos.setEditable(true);

    }//GEN-LAST:event_btnActualizarPActionPerformed
