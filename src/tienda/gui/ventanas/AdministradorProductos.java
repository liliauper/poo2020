    private void tblProductosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblProductosMouseClicked
        List<Producto> productos = prodRepository.getAll();
        int row = tblProductos.rowAtPoint(evt.getPoint());
        if (row >= 0) {
            Producto elegido = productos.get(row);
            String url = elegido.getImagen();
            cargarImagen(jpImagen, url);
        }
    }//GEN-LAST:event_tblProductosMouseClicked
    public void cargarImagen(JPanel pnl, String url) {
        int x = pnl.getWidth();
        int y = pnl.getHeight();
        pnl.removeAll();
        Imagen img = new Imagen(x, y, url);
        if (!url.isBlank() && !img.exists()) {
            pnl.add(img);
        }
        pnl.repaint();
    }

