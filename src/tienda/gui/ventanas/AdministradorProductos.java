    private void btnVerEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVerEliminarActionPerformed

        lblInfo.setText("");
        if (txtIdEliminar.getText().equals("")) {
            lblInfo.setText("Ingresa id de articulo que quieres eliminar");
            return;

        }

        int id = Integer.parseInt(txtIdEliminar.getText());
        Producto producto = prodRepository.getById(id);
        if (producto.getId() == 0) {
            lblInfo.setText("No existe articulo con Id: " + id);
            limpiarFormEliminarProductos();
            return;
        }
        try {
            txtCodigoE.setText(Integer.toString(producto.getCod_productos()));
            txtNombreE.setText(producto.getNombre());
            txtPrecioE.setText(Float.toString(producto.getPrecio()));
            txtCantidadE.setText(Integer.toString(producto.getCantidad()));
            txtImagenE.setText(producto.getImagen());
            artxtDescripcionE.setText(producto.getDescripcion());
            artxtDetallesE.setText(producto.getDetalles());
            cmbDisponibleE.setSelectedItem(producto.getDisponible());
            txtIdEliminar.setEditable(false);
        } catch (Exception e) {

            e.printStackTrace();
        }


    }//GEN-LAST:event_btnVerEliminarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed

        // Eliminar articulo
        int id = Integer.parseInt(txtIdEliminar.getText());

        Producto producto = prodRepository.getById(id);
        if (producto.getId() == 0) {
            lblInfo.setText("No existe articulo con Id: " + id);
            return;
        }
        if (JOptionPane
                .showConfirmDialog(this, "Estas seguro "
                        + "que quieres eliminar articulo id:" + id
                        + "?") != 0) {
            return;
        }
        prodRepository.remove(producto);
        lblInfo.setText(producto.getNombre() + ", id: " + id + ", fue removido con exito");

        limpiarFormEliminarProductos();
        txtIdEliminar.setEditable(true);
    }//GEN-LAST:event_btnEliminarActionPerformed

